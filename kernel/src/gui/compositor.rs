/// A snapshot of a window's state captured while the `WINDOW_REGISTRY` lock is
/// held.  Once copied out we can draw without contending on the lock.
struct WindowSnapshot {
    handle: WindowHandle,
    x: i32,
    y: i32,
    width: u32,
    height: u32,
    client_x: i32,
    client_y: i32,
    client_width: u32,
    client_height: u32,
    title: String,
    focused: bool,
    bg_color: u32,
    style: WindowStyle,
    state: WindowState,
}

