/// Render a single character onto a surface
pub fn draw_char(
    surface: &mut Surface,
    x: i32,
    y: i32,
    ch: char,
    fg_color: u32,
    bg_color: u32,
    bg_mode: BgMode,
) {
    let c = ch as u32;
    if c < 0x20 || c > 0x7E {
        return; // unprintable
    }
    let glyph_offset = ((c - 0x20) as usize) * 16;
    let sw = surface.width as i32;
    let sh = surface.height as i32;

    for row in 0..16i32 {
        let py = y + row;
        if py < 0 || py >= sh {
            continue;
        }
        let byte = VGA_FONT_8X16[glyph_offset + row as usize];
        for col in 0..8i32 {
            let px = x + col;
            if px < 0 || px >= sw {
                continue;
            }
            let lit = (byte >> (7 - col)) & 1 != 0;
            if lit {
                surface.pixels[py as usize * surface.width as usize + px as usize] = fg_color;
            } else if bg_mode == BgMode::Opaque {
                surface.pixels[py as usize * surface.width as usize + px as usize] = bg_color;
            }
        }
    }
}

/// Render a text string onto a surface
pub fn text_out(surface: &mut Surface, dc: &DeviceContext, x: i32, y: i32, text: &str) {
    let (ox, oy) = (dc.origin_x, dc.origin_y);
    let mut cx = x + ox;
    let cy = y + oy;

    for ch in text.chars() {
        if ch == '\n' {
            // newlines not handled in single-line text_out
            continue;
        }
        draw_char(surface, cx, cy, ch, dc.text_color, dc.bg_color, dc.bg_mode);
        cx += FONT_WIDTH as i32;
    }
}

/// Measure text dimensions (width, height) without drawing
pub fn measure_text(text: &str) -> (u32, u32) {
    let len = text.chars().filter(|c| *c >= ' ' && *c <= '~').count();
    (len as u32 * FONT_WIDTH, FONT_HEIGHT)
}
